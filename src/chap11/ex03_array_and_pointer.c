#include <stdio.h>

/*
[ex03] 배열과 포인터의 관계
------------------------------------------------------------
핵심
1) 배열 이름 a는 첫 원소의 주소(&a[0])로 변환된다.
2) a + 1은 다음 원소의 주소이고, *(a+1)은 그 값을 의미한다.
3) 포인터 p도 배열처럼 p[i] 형식으로 접근할 수 있다.
------------------------------------------------------------
슬라이드 키워드
- 포인터와 배열
- 배열 이름과 주소
- 인덱스 표기 vs 포인터 표기
------------------------------------------------------------
*/

int main(void)
{
    int a[] = {10, 20, 30, 40, 50};
    int *p = a;

    printf("&a[0] = %p\n", (void *)&a[0]);
    printf("&a[1] = %p\n", (void *)&a[1]);
    printf("a      = %p\n\n", (void *)a);

    // a는 &a[0]으로 변환되며, a+1은 다음 원소의 주소이다.
    // 배열 이름 a 자체는 대입할 수 없는 "고정된 주소"이다.
    printf("a + 1  = %p\n", (void *)(a + 1));
    printf("*a     = %d\n", *a);
    printf("*(a+1) = %d\n\n", *(a + 1));

    // 포인터도 배열처럼 p[i]로 접근 가능
    printf("a[0]=%d a[1]=%d a[2]=%d\n", a[0], a[1], a[2]);
    printf("p[0]=%d p[1]=%d p[2]=%d\n\n", p[0], p[1], p[2]);

    // p로 값을 바꾸면 실제 배열 a가 바뀐다.
    p[0] = 60;
    p[1] = 70;
    p[2] = 80;

    printf("a[0]=%d a[1]=%d a[2]=%d\n", a[0], a[1], a[2]);
    printf("p[0]=%d p[1]=%d p[2]=%d\n", p[0], p[1], p[2]);

    return 0;
}

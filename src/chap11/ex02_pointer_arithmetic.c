#include <stdio.h>

/*
[ex02] 포인터 연산과 메모리 보기
------------------------------------------------------------
핵심
1) 포인터 + 1은 타입 크기만큼 이동한다.
2) (*p)++ 는 값을 증가, p++ 는 주소를 증가한다.
3) int 데이터를 char*로 보면 바이트 단위로 관찰할 수 있다.
------------------------------------------------------------
슬라이드 키워드
- 포인터 연산
- *p++ vs (*p)++
- 포인터 형변환(바이트 관찰)
------------------------------------------------------------
*/

int main(void)
{
    // (1) 타입에 따른 포인터 이동
    int ai[3] = {10, 20, 30};
    double ad[3] = {1.1, 2.2, 3.3};

    int *pi = ai;
    double *pd = ad;

    // int 포인터는 4바이트 단위(환경에 따라 다름),
    // double 포인터는 8바이트 단위(환경에 따라 다름)로 이동한다.
    printf("pi = %p, pi+1 = %p\n", (void *)pi, (void *)(pi + 1));
    printf("pd = %p, pd+1 = %p\n\n", (void *)pd, (void *)(pd + 1));

    // (2) 값 증가 vs 주소 증가
    int x = 10;
    pi = &x;
    // (*pi)++ : 값 증가
    (*pi)++;
    printf("after (*pi)++ : x=%d, pi=%p\n", x, (void *)pi);
    // pi++ : 주소 증가(주의: x를 벗어나는 주소)
    // 이동 후 역참조하면 정의되지 않은 동작이므로 하지 않는다.
    pi++;
    printf("after pi++     : pi=%p\n\n", (void *)pi);

    // (3) 메모리 바이트 관찰 (엔디안 확인)
    int data = 0x0A0B0C0D;
    char *pc = (char *)&data;
    for (int i = 0; i < (int)sizeof(int); i++) {
        // char*는 1바이트씩 접근하므로 메모리 배열을 그대로 볼 수 있다.
        printf("byte[%d] = %02X\n", i, (unsigned char)*(pc + i));
    }

    return 0;
}

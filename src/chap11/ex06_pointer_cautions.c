// 올바른 예: 외부에서 전달받은 주소에 값 채우기
void fill_value(int *p)
{
    if (p == NULL) return;
    *p = 10;
}

int main(void)
{
    // (1) 초기화되지 않은 포인터 사용 금지
    // int *p; *p = 100; // 위험!

    // (2) 타입 불일치 예시
    // int i; double *pd = &i; // 오류!

    // (3) 안전한 사용
    int value = 0;
    fill_value(&value);
    printf("value = %d\n", value);

    // (4) 메모리 매핑 하드웨어(개념)
    // volatile int *hw = (volatile int *)0x7FFF; // 예시 주소
    // *hw = 0x0001; // 실제 하드웨어에 쓰는 코드
    // 일반 프로그램에서는 이런 주소 접근을 하지 않는다.

    return 0;
}

#include <stdio.h>

/*
[ex01] 문자열 기본: 문자 배열과 문자열 상수
------------------------------------------------------------
핵심
1) 문자열은 문자 배열 끝에 '\0'이 있어야 한다.
2) "문자열"은 자동으로 마지막에 '\0'이 들어간다.
3) 문자열 리터럴은 읽기 전용으로 취급하는 것이 안전하다.
------------------------------------------------------------
슬라이드 키워드
- 문자열 표현
- NULL 문자('\0')
- 문자열 리터럴
------------------------------------------------------------
*/

int main(void)
{
    // (1) 문자 배열로 문자열 만들기
    char str[4];
    str[0] = 'a';
    str[1] = 'b';
    str[2] = 'c';
    str[3] = '\0'; // 문자열 끝 표시

    printf("str = %s\n", str);

    // (2) 문자열 리터럴로 초기화
    // 배열 크기는 "문자 수 + 1(\\0)" 이상이어야 한다.
    char str1[6] = "Seoul";              // 5글자 + '\0'
    char str2[3] = {'i', 's', '\0'};
    char str3[] = "the capital city of Korea.";
    printf("%s %s %s\n", str1, str2, str3);

    // (3) 문자열 리터럴 포인터
    // 리터럴은 읽기 전용 메모리에 저장될 수 있으므로 수정하지 않는다.
    const char *p = "HelloWorld";       // 리터럴은 읽기 전용
    printf("%s\n", p);
    p = "Goodbye";                       // 포인터가 가리키는 대상 변경
    printf("%s\n", p);

    return 0;
}
